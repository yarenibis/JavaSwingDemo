
public class sanatçı {
int id;
String ad;
String etkinlik;
public sanatçı(int id, String ad, String etkinlik) {
	super();
	this.id = id;
	this.ad = ad;
	this.etkinlik = etkinlik;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getAd() {
	return ad;
}
public void setAd(String ad) {
	this.ad = ad;
}
public String getEtkinlik() {
	return etkinlik;
}
public void setEtkinlik(String etkinlik) {
	this.etkinlik = etkinlik;
}


}
